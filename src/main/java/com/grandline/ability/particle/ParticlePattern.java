package com.grandline.ability.particle;

/**
 * Predefined particle patterns for abilities.
 */
public enum ParticlePattern {
    
    /**
     * Burst of particles in all directions.
     */
    BURST,
    
    /**
     * Ring of particles around the player.
     */
    RING,
    
    /**
     * Spiral pattern upwards.
     */
    SPIRAL,
    
    /**
     * Directional beam.
     */
    BEAM,
    
    /**
     * Trail behind player.
     */
    TRAIL,
    
    /**
     * Single point.
     */
    POINT
}
