package com.grandline.ability.sound;

/**
 * Types of sounds for abilities.
 */
public enum SoundType {
    
    /**
     * Sound played when ability activates.
     */
    ACTIVATION,
    
    /**
     * Looping sound during ability.
     */
    LOOP,
    
    /**
     * Sound on impact/hit.
     */
    IMPACT,
    
    /**
     * Ambient background sound.
     */
    AMBIENT
}
